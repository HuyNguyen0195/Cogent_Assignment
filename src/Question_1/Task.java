package Question_1;

public class Task{
	
	private int taskId;
	private String TaskName;
	public Task() {
	}
	// task ID
	public int getTaskId() {
		return taskId;
	}
	public void setTaskId(int taskId) {
		this.taskId = taskId;
	}
	// task name
	public String getTaskName() {
		return TaskName;
	}
	public void setTaskName(String taskName) {
		TaskName = taskName;
	}
	
}